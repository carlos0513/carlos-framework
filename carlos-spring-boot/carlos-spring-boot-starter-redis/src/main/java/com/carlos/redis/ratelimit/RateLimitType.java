package com.carlos.redis.ratelimit;

/**
 * 限流类型枚举
 * <p>
 * 定义不同的限流维度，支持全局限流、用户级限流、IP限流和自定义限流
 *
 * @author carlos
 * @date 2026-04-08
 */
public enum RateLimitType {

    /**
     * 全局限流 - 针对整个应用或接口的总流量限制
     * <p>适用于保护下游服务、防止系统过载</p>
     */
    GLOBAL,

    /**
     * 用户级限流 - 针对每个用户的独立限流
     * <p>需要配合用户标识使用，适用于防止刷单、API滥用</p>
     */
    USER,

    /**
     * IP限流 - 针对每个IP地址的独立限流
     * <p>适用于防止恶意攻击、爬虫</p>
     */
    IP,

    /**
     * 自定义限流 - 基于SpEL表达式的自定义限流键
     * <p>灵活性最高，可根据业务需求自定义限流维度</p>
     */
    CUSTOM
}
